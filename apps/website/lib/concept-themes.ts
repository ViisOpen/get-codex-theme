import type { CodexTheme } from "./themes";

export type ConceptCategory = "Mascots" | "Brand concepts" | "Gaming" | "Culture" | "Aesthetic";

export type ConceptTheme = CodexTheme & {
  category: ConceptCategory;
  artDirection: string;
  workspaceFit: string;
  previewReady: boolean;
  previewImage?: string;
  inspiration?: string;
  trademarkNotice?: string;
};

type ConceptSeed = {
  slug: string;
  name: string;
  category: ConceptCategory;
  mode: "dark" | "light";
  tagline: string;
  description: string;
  accent: string;
  accentStrong: string;
  secondary: string;
  tags: string[];
  artDirection: string;
  workspaceFit: string;
  previewReady?: boolean;
  inspiration?: string;
};

const brandNotice = (name: string) =>
  `${name} is a trademark of its respective owner. This independent concept is not sponsored, approved, or affiliated with that company; its brand mark identifies the visual direction.`;

function buildConcept(seed: ConceptSeed, index: number): ConceptTheme {
  const dark = seed.mode === "dark";
  return {
    ...seed,
    foreground: dark ? "#f7f8fb" : "#202226",
    muted: dark ? "#aeb3c0" : "#666b73",
    surface: dark ? "rgba(12, 14, 20, 0.88)" : "rgba(255, 255, 255, 0.86)",
    canvas: dark ? "#080a0f" : "#f2f2ef",
    gradient: `radial-gradient(circle at 82% 34%, ${seed.accent}cc, transparent 22%), radial-gradient(circle at 70% 72%, ${seed.secondary}aa, transparent 32%), linear-gradient(140deg, ${dark ? "#05060a" : "#faf9f5"} 0%, ${seed.accentStrong} 145%)`,
    focusX: 79,
    focusY: 48,
    order: 1_000 + index * 10,
    previewReady: Boolean(seed.previewReady),
    previewImage: seed.previewReady ? `/concept-themes/${seed.slug}/background.jpg` : undefined,
    trademarkNotice: seed.inspiration ? brandNotice(seed.inspiration) : undefined,
  };
}

