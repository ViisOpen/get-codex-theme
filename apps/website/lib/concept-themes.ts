export const conceptThemes = seeds.map(buildConcept);

export function getConceptTheme(slug: string) {
  return conceptThemes.find((theme) => theme.slug === slug);
}

export function getRelatedConceptThemes(theme: ConceptTheme, limit = 4) {
  const sameCategory = conceptThemes.filter((candidate) => candidate.slug !== theme.slug && candidate.category === theme.category);
  const others = conceptThemes.filter((candidate) => candidate.slug !== theme.slug && candidate.category !== theme.category);
  return [...sameCategory, ...others].slice(0, limit);
}

